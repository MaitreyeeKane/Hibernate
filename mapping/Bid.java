package in.co.rays.mapping;

public class Bid {
private int id;
private int amount;
private int Itemid;
public Bid() {}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getAmount() {
	return amount;
}
public void setAmount(int amount) {
	this.amount = amount;
}
public int getItemid() {
	return Itemid;
}
public void setItemid(int itemid) {
	Itemid = itemid;
}

}
